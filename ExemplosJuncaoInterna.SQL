--EXEMPLOS Join 
SELECT D.NM_DEPTO,
    F.NM_FUNCIONARIO,
    F.VL_SALARIO
    FROM T_SIP_DEPARTAMENTO D INNER JOIN T_SIP_FUNCIONARIO F
    ON(D.CD_DEPTO = F.CD_DEPTO)
    --O D é o apelido para a tabela t_sip_departamento
    --o F é o apelido para a tabela t_sip_funcionario
    ORDER BY D.NM_DEPTO ;
    

